#!/usr/bin/env php
<?php

declare(strict_types=1);

/**
 * Regenerates .castor/mate.generated.php from `vendor/bin/mate mcp:tools:list --format=json`.
 *
 * Run from project root:
 *   php .castor/bin/generate-mate-castor-tasks.php
 *
 * Or: castor dev:mate-generate-castor
 *
 * Optional: MATE_TOOLS_LIST_JSON=/path/to/dump.json to skip running mate (CI / offline).
 */
$projectRoot = dirname(__DIR__, 2);
chdir($projectRoot);

$mateList = getenv('MATE_TOOLS_LIST_JSON');
if (\is_string($mateList) && is_file($mateList)) {
    $json = file_get_contents($mateList);
    if (false === $json) {
        fwrite(STDERR, "Cannot read MATE_TOOLS_LIST_JSON file: {$mateList}\n");
        exit(1);
    }
} else {
    $cmd = 'php vendor/bin/mate mcp:tools:list --format=json 2>/dev/null';
    $json = shell_exec($cmd);
    if (!\is_string($json) || '' === $json) {
        fwrite(STDERR, "Failed to run mate mcp:tools:list. From project root try: {$cmd}\n");
        exit(1);
    }
}

$data = json_decode($json, true);
if (!\is_array($data) || !isset($data['tools']) || !\is_array($data['tools'])) {
    fwrite(STDERR, "Unexpected JSON from mate (missing tools).\n");
    exit(1);
}

/** @var array<string, array<string, mixed>> $tools */
$tools = $data['tools'];
ksort($tools);

$out = [];
$out[] = '<?php';
$out[] = '';
$out[] = 'declare(strict_types=1);';
$out[] = '';
$out[] = '// AUTO-GENERATED by .castor/bin/generate-mate-castor-tasks.php — do not edit by hand.';
$out[] = '// Regenerate: php .castor/bin/generate-mate-castor-tasks.php  or  castor dev:mate-generate-castor';
$out[] = '';
$out[] = 'namespace mate;';
$out[] = '';
$out[] = 'use Castor\Attribute\AsArgument;';
$out[] = 'use Castor\Attribute\AsOption;';
$out[] = 'use Castor\Attribute\AsTask;';
$out[] = 'use function CastorTasks\mate_tool_exec;';
$out[] = '';

foreach ($tools as $toolId => $tool) {
    if (!\is_array($tool)) {
        continue;
    }
    $name = $tool['name'] ?? $toolId;
    if (!\is_string($name) || '' === $name) {
        $name = (string) $toolId;
    }
    $description = summarizeDescription($tool['description'] ?? '');
    $schema = $tool['input_schema'] ?? null;
    if (!\is_array($schema)) {
        $schema = null;
    }

    $func = toolFunctionName((string) $toolId);
    $taskDesc = escapePhpSingleQuotedString($description);
    $out[] = '#[AsTask(name: '.var_export($name, true).', namespace: \'mate\', description: \''.$taskDesc.'\')]';

    if (null === $schema || !isset($schema['properties']) || !\is_array($schema['properties']) || [] === $schema['properties']) {
        $out[] = 'function '.$func.'(): void';
        $out[] = '{';
        $out[] = '    mate_tool_exec('.var_export($name, true).');';
        $out[] = '}';
        $out[] = '';

        continue;
    }

    $block = buildToolFunctionLines($name, $schema['properties'], $schema['required'] ?? []);
    $out[] = 'function '.$func.'(';
    if ([] !== $block['params']) {
        $out[] = '    '.implode(",\n    ", $block['params']);
    }
    $out[] = '): void {';
    foreach ($block['body'] as $line) {
        $out[] = '    '.$line;
    }
    $out[] = '}';
    $out[] = '';
}

$target = $projectRoot.'/.castor/mate.generated.php';
$content = implode("\n", $out)."\n";
if (false === file_put_contents($target, $content)) {
    fwrite(STDERR, "Cannot write {$target}\n");
    exit(1);
}

echo "Wrote {$target} (".\count($tools)." tools)\n";

/**
 * @param array<string, mixed> $properties
 * @param mixed                $required
 *
 * @return array{params: list<string>, body: list<string>}
 */
function buildToolFunctionLines(string $mateToolName, array $properties, mixed $required): array
{
    $reqList = \is_array($required)
        ? array_values(array_filter($required, static fn ($r) => \is_string($r)))
        : [];

    $params = [];
    $payloadLines = [];

    foreach ($reqList as $propName) {
        if (!isset($properties[$propName]) || !\is_array($properties[$propName])) {
            continue;
        }
        $binding = parameterBinding($propName, $properties[$propName], true);
        if (null === $binding) {
            continue;
        }
        $params[] = $binding['signature'];
        $payloadLines[] = "'{$propName}' => {$binding['expr']},";
    }

    foreach ($properties as $propName => $propDef) {
        if (!\is_string($propName) || !\is_array($propDef)) {
            continue;
        }
        if (\in_array($propName, $reqList, true)) {
            continue;
        }
        $binding = parameterBinding($propName, $propDef, false);
        if (null === $binding) {
            continue;
        }
        $params[] = $binding['signature'];
        $payloadLines[] = "'{$propName}' => {$binding['expr']},";
    }

    $body = ['$payload = ['];
    foreach ($payloadLines as $pl) {
        $body[] = '    '.$pl;
    }
    $body[] = '];';
    $body[] = 'mate_tool_exec('.var_export($mateToolName, true).', $payload);';

    return ['params' => $params, 'body' => $body];
}

/**
 * @param array<string, mixed> $propDef
 *
 * @return array{signature: string, expr: string}|null
 */
function parameterBinding(string $propName, array $propDef, bool $isRequired): ?array
{
    $phpVar = phpVariableForSchemaProperty($propName);
    $types = normalizeJsonTypes($propDef['type'] ?? 'string');
    $hasNull = in_array('null', $types, true);
    $scalarTypes = array_values(array_filter($types, static fn (string $t) => 'null' !== $t));

    $default = $propDef['default'] ?? null;
    $desc = $propDef['description'] ?? '';
    $descStr = \is_string($desc) ? normalizeDescriptionText($desc) : '';
    $descArg = '' !== $descStr ? ', description: \''.escapePhpSingleQuotedString($descStr).'\'' : '';

    $kind = 'string';
    foreach ($scalarTypes as $t) {
        if ('boolean' === $t) {
            $kind = 'bool';
            break;
        }
        if ('integer' === $t) {
            $kind = 'int';
            break;
        }
        if ('number' === $t) {
            $kind = 'float';
            break;
        }
    }

    if (\in_array('array', $scalarTypes, true) || \in_array('object', $scalarTypes, true)) {
        $kind = 'json';
    }

    $optName = optionNameForSchemaProperty($propName);

    if ('json' === $kind) {
        $varName = $phpVar.'Json';
        $sig = $isRequired
            ? '#[AsArgument(name: \''.$optName.'\''.$descArg.')] string $'.$varName
            : '#[AsOption(name: \''.$optName.'\''.$descArg.')] ?string $'.$varName.' = null';
        $expr = $isRequired
            ? 'json_decode($'.$varName.', true, 512, JSON_THROW_ON_ERROR)'
            : '(null === $'.$varName.' || \'\' === $'.$varName.') ? null : json_decode($'.$varName.', true, 512, JSON_THROW_ON_ERROR)';

        return [
            'signature' => $sig,
            'expr' => $expr,
        ];
    }

    if ('bool' === $kind) {
        $def = \is_bool($default) ? ($default ? 'true' : 'false') : 'false';
        $sig = $isRequired
            ? '#[AsArgument(name: \''.$optName.'\''.$descArg.')] bool $'.$phpVar
            : '#[AsOption(name: \''.$optName.'\''.$descArg.')] bool $'.$phpVar.' = '.$def;

        return [
            'signature' => $sig,
            'expr' => '$'.$phpVar,
        ];
    }

    if ('int' === $kind) {
        if ($hasNull || null === $default) {
            $sig = $isRequired
                ? '#[AsArgument(name: \''.$optName.'\''.$descArg.')] ?int $'.$phpVar
                : '#[AsOption(name: \''.$optName.'\''.$descArg.')] ?int $'.$phpVar.' = null';

            return [
                'signature' => $sig,
                'expr' => '$'.$phpVar,
            ];
        }
        $def = \is_int($default) ? (string) $default : '0';
        $sig = $isRequired
            ? '#[AsArgument(name: \''.$optName.'\''.$descArg.')] int $'.$phpVar
            : '#[AsOption(name: \''.$optName.'\''.$descArg.')] int $'.$phpVar.' = '.$def;

        return [
            'signature' => $sig,
            'expr' => '$'.$phpVar,
        ];
    }

    if ('float' === $kind) {
        $defNum = (\is_float($default) || \is_int($default)) ? (string) (float) $default : '0.0';
        $sig = $isRequired
            ? '#[AsArgument(name: \''.$optName.'\''.$descArg.')] float $'.$phpVar
            : '#[AsOption(name: \''.$optName.'\''.$descArg.')] float $'.$phpVar.' = '.$defNum;

        return [
            'signature' => $sig,
            'expr' => '$'.$phpVar,
        ];
    }

    if ($hasNull) {
        $sig = $isRequired
            ? '#[AsArgument(name: \''.$optName.'\''.$descArg.')] ?string $'.$phpVar
            : '#[AsOption(name: \''.$optName.'\''.$descArg.')] ?string $'.$phpVar.' = null';

        return [
            'signature' => $sig,
            'expr' => '$'.$phpVar,
        ];
    }

    $defVal = \is_string($default) ? $default : '';
    $sig = $isRequired
        ? '#[AsArgument(name: \''.$optName.'\''.$descArg.')] string $'.$phpVar
        : '#[AsOption(name: \''.$optName.'\''.$descArg.')] string $'.$phpVar.' = \''.escapePhpSingleQuotedString($defVal).'\'';

    return [
        'signature' => $sig,
        'expr' => '$'.$phpVar,
    ];
}

function phpVariableForSchemaProperty(string $propName): string
{
    if ('version' === $propName) {
        return 'constraint';
    }

    $reserved = [
        'array', 'list', 'default', 'function', 'class', 'namespace', 'use', 'new', 'true', 'false', 'null',
        'or', 'and', 'xor', 'match', 'mixed', 'void', 'never', 'static', 'self', 'parent',
    ];
    if (\in_array(strtolower($propName), $reserved, true)) {
        return $propName.'_';
    }

    return $propName;
}

function optionNameForSchemaProperty(string $propName): string
{
    return 'version' === $propName ? 'constraint' : $propName;
}

/**
 * @return list<string>
 */
function normalizeJsonTypes(mixed $type): array
{
    if (\is_string($type)) {
        return [$type];
    }
    if (!\is_array($type)) {
        return ['string'];
    }
    $out = [];
    foreach ($type as $t) {
        if (\is_string($t)) {
            $out[] = $t;
        }
    }

    return [] !== $out ? $out : ['string'];
}

function toolFunctionName(string $toolId): string
{
    $base = preg_replace('/[^a-zA-Z0-9_]+/', '_', str_replace('-', '_', $toolId));
    $base = (string) $base;
    $base = trim($base, '_');
    if ('' === $base) {
        $base = 'tool';
    }
    if (preg_match('/^\d/', $base)) {
        $base = 'tool_'.$base;
    }

    return 'tool_'.$base;
}

function summarizeDescription(?string $d): string
{
    if (null === $d || '' === $d) {
        return 'Mate MCP tool';
    }

    return normalizeDescriptionText($d);
}

/**
 * Collapses whitespace for embedding in PHP string literals; no length truncation so
 * `castor mate:* --help` shows full Mate tool and option descriptions.
 */
function normalizeDescriptionText(string $d): string
{
    $d = trim(str_replace(["\n", "\r"], ' ', $d));

    return (string) preg_replace('/\s+/', ' ', $d);
}

function escapePhpSingleQuotedString(string $s): string
{
    return str_replace(['\\', "'"], ['\\\\', "\\'"], $s);
}
